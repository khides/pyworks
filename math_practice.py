import math

print(1,math.fabs(-1.5))###絶対値を返す

print(2,math.factorial(10))##階乗を返す

print(3,math.pi)##円周率

print(4,math.ceil(4.2))###切り上げ

print(5,math.floor(4.6))###切り捨て

print(6,math.sqrt(5))###平方ルートを返す

print(7,math.gcd(18,30))###最大公約数

print(8,math.exp(2))###eの階乗を返す

print(9,math.log10(100))###常用対数

print(10,math.log(16,2))###対数

print(11,math.sin(math.pi/2))###正弦

print(12,math.cos(math.pi/2))###余弦

print(13,math.tan(math.pi/4))###正接

print(14,math.asin(1))###正弦逆関数

print(15,math.acos(0))###余弦逆関数

print(16,math.atan(1))###正接逆関数
